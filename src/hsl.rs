pub fn hue_to_rgb(h: f32) -> (f32, f32, f32) {
    let x = 1.0 - ((h / 60.0) % 2.0 - 1.0).abs();
    match h {
        0.0..=60.0 => (1.0, x, 0.0),
        60.0..=120.0 => (x, 1.0, 0.0),
        120.0..=180.0 => (0.0, 1.0, x),
        180.0..=240.0 => (0.0, x, 1.0),
        240.0..=300.0 => (x, 0.0, 1.0),
        _ => (1.0, 0.0, x),
    }
}
